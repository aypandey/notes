package com.demo.notes.model;

public class TagDTO {
	private String tagName;
	
	public TagDTO() {
		super();
	}

	public TagDTO(String tagName) {
		super();
		this.tagName = tagName;
	}

	public String getTagName() {
		return tagName;
	}

	public void setTagName(String tagName) {
		this.tagName = tagName;
	}
	
	
}
